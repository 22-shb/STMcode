float ADC2Resistance(uint32_t adc_value)
{
  return (adc_value / (4096.0f - adc_value)) * 10000.0f;
}
/**
 * @brief 通过NTC电阻反推温度
 *
 * @param R1 NTC电阻值
 * @retval 返回温度，float类型，单位摄氏度
 */
float resistance2Temperature(float R1)
{
  float B = 3950.0f;
  float R2 = 10000.0f;
  float T2 = 25.0f;
  return (1.0 / ((1.0 / B) * log(R1 / R2) + (1.0 / (T2 + 273.15))) - 273.15);
}
